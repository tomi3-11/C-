#include <stdio.h>
#include <stdbool.h>

int main() {
	// Type conversion in action using Assignment
	// Initialization of x
	bool x = true;

	// bool to integer
	int y = x;

	// bool to float
	float z = x;

	printf("Display x %d.\n", x);
	printf("Display y %d.\n", y);
	printf("Display z %f.\n", z);

}
